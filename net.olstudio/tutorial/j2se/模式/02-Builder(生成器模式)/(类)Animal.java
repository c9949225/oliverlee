public class Animal {
	private String name;
	private String meat;
	private int legs;

	public String getName() {
		return name;
	}

	public String getMeat() {
		return meat;
	}

	public int getLegs() {
		return legs;
	}

	public void setName(String aName) {
		name = aName;
	}

	public void setMeat(String aMeat) {
		meat = aMeat;
	}

	public void setLegs(int alegs) {
		legs = alegs;
	}
}